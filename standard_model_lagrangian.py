# Section 4: Higgs ghosts (Goldstone modes)
# ----------------------------------------------------------------------
def section_4_higgs_ghosts() -> None:
    header("SECTION 4 — Higgs ghosts: virtual artifacts of the Higgs field")

    lam_c, v = sp.symbols("lambda v", positive=True)
    H, phi0 = sp.symbols("H phi^0", real=True)
    phip = sp.Symbol("phi^+")  # complex charged Goldstone

    # Full Higgs doublet: physical Higgs H plus the "ghost" fields
    # phi+ and phi0 that appear all over Gutierrez's transcription.
    phi = sp.Matrix([phip, (v + H + I * phi0) / sqrt(2)])

    phi2 = sp.expand(simplify((phi.H * phi)[0]))
    mu2 = lam_c * v**2  # minimization condition of V = -mu^2|phi|^2 + lam|phi|^4
    V = sp.expand(-mu2 * phi2 + lam_c * phi2**2)

    print("\nWrite the Higgs doublet around its vacuum as")
    print("    phi = ( phi+ , (v + H + i phi0)/sqrt(2) )")
    print("and expand the potential V = -mu^2 |phi|^2 + lambda |phi|^4")
    print("(using the minimum condition mu^2 = lambda v^2).")
    print("\nQuadratic (mass) terms of the expansion:")

    # Pure mass terms: take the quadratic coefficient of each field with
    # every OTHER field switched off (otherwise quartic cross-terms leak in).
    quad = {
        "H^2      (physical Higgs boson)":
            V.coeff(H, 2).subs({phi0: 0, phip: 0}),
        "phi0^2   (neutral Higgs ghost)":
            V.coeff(phi0, 2).subs({H: 0, phip: 0}),
        "phi+phi- (charged Higgs ghosts)":
            V.coeff(phip * conjugate(phip)).subs({H: 0, phi0: 0}),
    }
    quad = {label: simplify(coeff) for label, coeff in quad.items()}
    for label, coeff in quad.items():
        print(f"    coefficient of {label:<34}: {sp.sstr(coeff)}")

    mH = simplify(sqrt(2 * quad["H^2      (physical Higgs boson)"]))
    print(f"\n    =>  m_H = {sp.sstr(mH)} = sqrt(2 lambda) v   "
          "(one massive Higgs boson)")
    print("    =>  phi0, phi+, phi- are MASSLESS Goldstone modes.")
    print("""
In the gauge used in the article's equation, these three modes appear as
independent 'Higgs ghost' fields with their own interaction terms —
Section 4 of the transcription. Physically they are not new particles:
they are the degrees of freedom the W+, W- and Z bosons 'eat' to become
massive (a massless vector has 2 polarizations; a massive one needs 3).""")


