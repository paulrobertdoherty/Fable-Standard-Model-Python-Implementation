# Section 3: Matter particles and the weak force
# ----------------------------------------------------------------------
def section_3_matter() -> None:
    header("SECTION 3 — Three generations of matter, the weak force, "
           "and Higgs couplings")

    y, v = sp.symbols("y v", positive=True)
    print("\nA Yukawa term  -y (psibar_L phi psi_R + h.c.)  turns into a mass")
    print("term when the Higgs settles at phi -> v/sqrt(2):")
    m = y * v / sqrt(2)
    print(f"\n    m_fermion = y v / sqrt(2) = {sp.sstr(m)}")

    v_num = 246.22  # GeV, Higgs vacuum expectation value
    fermions = {  # masses in GeV (PDG values)
        "electron": 0.000511, "muon": 0.10566, "tau": 1.77686,
        "up": 0.00216, "charm": 1.27, "top": 172.69,
        "down": 0.00467, "strange": 0.0934, "bottom": 4.18,
    }
    print(f"\nInverting for the Yukawa couplings (v = {v_num} GeV):")
    print(f"    {'fermion':<10}{'mass (GeV)':>14}{'Yukawa y':>14}")
    for name, mass in fermions.items():
        yy = mass * np.sqrt(2) / v_num
        print(f"    {name:<10}{mass:>14.6g}{yy:>14.3g}")
    print("    " + "-" * 38)
    print(f"    {'neutrinos':<10}{'0 (assumed)':>14}{'0':>14}   <-- the equation's")
    print("                                              known flaw: neutrino")
    print("                                              oscillations prove m > 0.")

    print("\nThe weak force also mixes the three quark generations through the")
    print("CKM matrix, which lets heavy quarks decay into lighter ones:")

    # Wolfenstein parametrization (PDG central values).
    lam, Aw, rho, eta = 0.2250, 0.826, 0.159, 0.348
    V = np.array([
        [1 - lam**2 / 2, lam, Aw * lam**3 * (rho - 1j * eta)],
        [-lam, 1 - lam**2 / 2, Aw * lam**2],
        [Aw * lam**3 * (1 - rho - 1j * eta), -Aw * lam**2, 1],
    ])
    rows, cols = ["u", "c", "t"], ["d", "s", "b"]
    print("\n    |V_CKM| (magnitudes):")
    print("          " + "".join(f"{c:>9}" for c in cols))
    for r, row in zip(rows, np.abs(V)):
        print(f"       {r}  " + "".join(f"{x:>9.4f}" for x in row))

    unitarity = np.max(np.abs(V.conj().T @ V - np.eye(3)))
    print(f"\n    Unitarity check  max|V†V - 1| = {unitarity:.1e}")
    print("    (~lambda^4 — exactly the accuracy of this truncated")
    print("     parametrization; the full CKM matrix is exactly unitary.)")
    print("    The tiny complex phase (eta) is the Standard Model's only")
    print("    source of CP violation — matter/antimatter asymmetry.")


