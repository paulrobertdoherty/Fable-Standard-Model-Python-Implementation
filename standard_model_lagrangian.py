# Section 5: Faddeev–Popov ghosts
# ----------------------------------------------------------------------
def section_5_faddeev_popov() -> None:
    header("SECTION 5 — Faddeev–Popov ghosts: cancelling gauge redundancies")

    print("""
Quantizing a gauge theory by summing over 'all field configurations'
overcounts, because many configurations are the same physics written in
different gauges. The fix (Faddeev & Popov, 1967) is to add fictitious
anticommuting scalar fields — ghosts cbar^a, c^a, one per gauge boson —
whose loop contributions exactly cancel the overcounting:

    L_ghost = cbar^a ( -d^2 delta^{ac} - g f^{abc} A^b_u d^u ) c^c

Note the SAME structure constants f^{abc} from Section 1 reappear: the
ghosts must know about the gauge group to cancel its redundancy. In the
article's equation these are the fields written as eta (and the X's for
the electroweak sector). Ghosts never appear as external, detectable
particles — they live only inside loops.

Ghost bookkeeping (one ghost/antighost pair per gauge boson):""")
    bosons = [("gluons (SU(3))", 8), ("weak bosons W1,W2,W3 (SU(2))", 3),
              ("hypercharge B (U(1))", 1)]
    total = 0
    for name, n in bosons:
        total += n
        print(f"    {name:<32} -> {n} ghost pair{'s' if n > 1 else ''}")
    print(f"    {'TOTAL':<32} -> {total} ghost pairs")
    print("""
(The U(1) ghost decouples in an abelian theory — its f^{abc} = 0 — which
is why plain electromagnetism never needed ghosts in the first place.)""")


